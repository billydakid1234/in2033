private void loadUsersTable() {
    try {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<User> users = loginAPI.getAllUsers();

        for (User user : users) {
            model.addRow(new Object[]{
                user.getUserId(),
                user.getUsername(),
                user.getRoleName(),
                user.getCreatedAt()
            });
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading users: " + e.getMessage());
    }
}

private void filterUsers() {
    try {
        String searchText = jTextField1.getText().trim().toLowerCase();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        List<User> users = loginAPI.getAllUsers();

        for (User user : users) {
            String username = user.getUsername().toLowerCase();
            String role = user.getRoleName().toLowerCase();

            if (searchText.isEmpty() || username.contains(searchText) || role.contains(searchText)) {
                model.addRow(new Object[]{
                    user.getUserId(),
                    user.getUsername(),
                    user.getRoleName(),
                    user.getCreatedAt()
                });
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Search error: " + e.getMessage());
    }
}
    
    
