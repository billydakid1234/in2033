    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        String productId = txtProductID.getText().trim();

        if (productId.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Enter a Product ID.");
            return;
            
        }
        
        boolean validProduct = true;
        //temporarily have this until connected with backend to look up if the product with id exists
        //as well as if the product is in stock in SA - will check from the catalogue
        //code calling the method in backend added here instead
        
        
        if (!validProduct) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid Product ID.");
            return;
        }
        
        String q = javax.swing.JOptionPane.showInputDialog(this, "Enter quantity:");

        if (q == null) return;

        int quantity;

        try {
            quantity = Integer.parseInt(q);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid quantity.");
            return;
        }

        if (quantity <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Quantity must be above 0!");
            return;
        }
        
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTableOrder.getModel();

        model.addRow(new Object[]{productId, quantity});
        
        txtProductID.setText("");
        
        
        
        
        
        
    }//GEN-LAST:event_btnAddItemActionPerformed

