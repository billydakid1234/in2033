private void loadTemplateKeys() {
    try {
        jComboBox2.removeAllItems();
        Map<String, String> templates = templateAPI.getAllTemplates();

        for (String key : templates.keySet()) {
            jComboBox2.addItem(key);
        }

        if (jComboBox2.getItemCount() > 0) {
            jComboBox2.setSelectedIndex(0);
            loadSelectedTemplate();
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading template keys: " + e.getMessage());
    }
}

private void loadSelectedTemplate() {
    try {
        String key = (String) jComboBox2.getSelectedItem();

        if (key == null || key.isBlank()) {
            return;
        }

        String content = templateAPI.getTemplate(key);
        jTextArea1.setText(content);
        jTextArea1.setCaretPosition(0);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading template: " + e.getMessage());
    }
}    
    
    
