    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
    String key = (String) jComboBox2.getSelectedItem();
    String value = jTextArea1.getText();

    if (key == null || key.isBlank()) {
        JOptionPane.showMessageDialog(this, "Please select a template.");
        return;
    }

    if (value == null || value.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Template content cannot be empty.");
        return;
    }

    boolean updated = templateAPI.updateTemplate(key, value);

    if (updated) {
        JOptionPane.showMessageDialog(this, "Template updated successfully.");
    } else {
        JOptionPane.showMessageDialog(this, "Template update failed.");
    }

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error saving template: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

